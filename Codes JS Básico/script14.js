
// Estrutuda de condição switch.

// Ideal para quando é necessário o uso de diversas condições.

const item = 4

switch(item) {
    case 1:
        console.log('Item 1 - setor de brinquedos')
        break
    case 2:
        console.log('Item 2 - setor de maquiagem')
        break
    case 3:
        console.log('Item 3 - setor de calçados')
        break
    case 4:
        console.log('Item 4 - setor de eletrodomésticos')
        break
    case 5:
        console.log('Item 5 - setor de roupas')
    case 6:
        console.log('Item 3 - setor de vestuário')
        break
    default:
        console.log('Numero informado é iválido')
}